/*wap to print single character*/

#include<stdio.h>

void main()
{
	//Char Data Type in store single character...
	char ch='r';

	printf("%c\n",ch);	// Print the character...

	ch ='M';
	printf("%c\n",ch);	//%c is used for character...

	ch ='w';
	printf("%c\n",ch);

	ch ='c';
	printf("%c\n",ch);

}